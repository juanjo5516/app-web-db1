    public function verify($confirmation_code)
    {
        $user = User::where('confirmation_code',$confirmation_code)->first();

        if($user == null){
            return Redirect::to('/');
        }else if($user->confirmed == 0){
            return view('auth/verify',compact('user'));
        }
    }
